export function PerimetroPentagono(lado){
    lado = prompt ('Ingrese lado del pentaogno  para calcular perimetro de pentagono;','');
    return lado * 5
}
export function AreaPentagono(area, apo){
    area = prompt ('Ingrese valor del perimetro del pentagono  para calcular Area;','');
    apo = prompt ('Ingrese altura del triangulo  para calcular Area de triangulo:','');
    return area * apo/ 2  
}