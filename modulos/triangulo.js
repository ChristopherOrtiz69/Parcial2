export function PerimetroTriangulo(lado){
    lado = prompt ('Ingrese valor para cualcular perimetro del triangulo:','');
    return lado * 3
}
export function AreaTriangulo(base, altura ){
    base = prompt ('Ingrese base del triangulo  para calcular Area de triangulo;','');
    altura = prompt ('Ingrese altura del triangulo  para calcular Area de triangulo:','');
    return base * altura / 2
}